#include <iostream>
using namespace std;


int main(){
    cout<<"This is a program to print out numbers between from 10~1 in descending order using for loops"<<endl;
    cout<<"This is not a drill evacuate the building."<<endl;

    for(int i=10; i>=1; i--){
        cout<<i<<" ";
    }
    cout<<"\n";
    cout<<"Blast off";




    return 0;
}